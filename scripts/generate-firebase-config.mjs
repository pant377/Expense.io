import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const envPath = resolve('.env');
const outputPath = resolve('src/environments/firebase.generated.ts');

function parseEnv(contents) {
  return Object.fromEntries(
    contents
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter((line) => line && !line.startsWith('#'))
      .map((line) => {
        const separator = line.indexOf('=');
        const key = separator === -1 ? line : line.slice(0, separator);
        const value = separator === -1 ? '' : line.slice(separator + 1);
        return [key.trim(), value.trim()];
      }),
  );
}

let fileEnvironment = {};

try {
  fileEnvironment = parseEnv(readFileSync(envPath, 'utf8'));
} catch {
  // CI can provide the values directly through process.env.
}

const environment = { ...fileEnvironment, ...process.env };
const requiredValues = {
  apiKey: environment.FIREBASE_API_KEY,
  authDomain: environment.FIREBASE_AUTH_DOMAIN,
  projectId: environment.FIREBASE_PROJECT_ID,
  storageBucket: environment.FIREBASE_STORAGE_BUCKET,
  messagingSenderId: environment.FIREBASE_MESSAGING_SENDER_ID,
  appId: environment.FIREBASE_APP_ID,
};

const missing = Object.entries(requiredValues)
  .filter(([, value]) => !value)
  .map(([key]) => key);

if (missing.length) {
  console.error(
    `Missing Firebase configuration: ${missing.join(', ')}. Copy .env.example to .env and fill it in.`,
  );
  process.exit(1);
}

const source = `// Generated by scripts/generate-firebase-config.mjs. Do not commit this file.
export const firebaseConfig = ${JSON.stringify(requiredValues, null, 2)} as const;
`;

writeFileSync(outputPath, source, 'utf8');
console.log('Generated local Firebase configuration.');
